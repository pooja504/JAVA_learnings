
public class Person {

    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public Person(String name) {
        this(name,18);
    }

    public Person(){

    }


    public int getAge() {
        return age;
    }

    public Person setAge(int age) {
        this.age = age;
        return this;
    }

    public String getName() {
        return name;
    }

    public Person setName(String name) {
        this.name = name;
        return this;
    }

    @Override
    public String toString() {
        return name+ " "+age;
    }

    @Override
    public boolean equals(Object obj) {
        Person objj= (Person) obj;
        return this.name==objj.name;
    }
}
